
public class Prodotto {

	private int id_prodotto;
	private String nome_prodotto;
	private int disponibilita_prodotto;
	private double prezzo_prodotto;
	
	public Prodotto(int id_prodotto, String nome_prodotto, int disponibilita_prodotto, double prezzo_prodotto) {
		this.id_prodotto = id_prodotto;
		this.nome_prodotto = nome_prodotto;
		this.disponibilita_prodotto = disponibilita_prodotto;
		this.prezzo_prodotto = prezzo_prodotto;
	}
	
	
}
